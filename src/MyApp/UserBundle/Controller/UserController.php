    }

    public function adminhelloAction() {

        die('hello admin');
        return $this->render('MyAppUserBundle:User:adminhello.html.twig');
    }

    public function afficherEtatCompteFormateurAction() {
        $em = $this->getDoctrine()->getManager();

        $entity = new User();

        $reposotory = $em->getRepository('MyAppUserBundle:User');
        $query = $reposotory->createQueryBuilder('u');
        $query->select('u')->where('u.enabled=0')->orderBy('u.id');
        $entity = $query->getQuery()->getResult();


        return $this->render('MyAppUserBundle:User:afficherFormateur.html.twig', array(
                    'entities' => $entity,
        ));
    }

    public function ValiderFormateurComiteAction($id) {
        //  $u=$userManager->get($id);
        //$user->setEnabled(true);
        $em = $this->getDoctrine()->getManager();
        $entity = new User();
        $entity = $em->getRepository('MyAppUserBundle:User')->find($id);
        $entity->setEnabled(true);
        $em->flush();
        return $this->render('MyAppUserBundle:User:ValiderFormateurComite.html.twig', array(
                    'entities' => $entity,
        ));
    }

}
